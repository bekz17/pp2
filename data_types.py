# Text
x = "Hello World"
print(x, type(x))

# Integer
x = 20
print(x, type(x))

# Float
x = 20.5
print(x, type(x))

# Complex
x = 1j
print(x, type(x))

# List
x = ["apple", "banana", "cherry"]
print(x, type(x))

# Tuple
x = ("apple", "banana", "cherry")
print(x, type(x))

# Range
x = range(6)
print(list(x), type(x))

# Dictionary
x = {"name": "John", "age": 36}
print(x, type(x))

# Set
x = {"apple", "banana", "cherry"}
print(x, type(x))

# Frozenset
x = frozenset({"apple", "banana", "cherry"})
print(x, type(x))

# Boolean
x = True
print(x, type(x))

# Bytes
x = b"Hello"
print(x, type(x))

# Bytearray
x = bytearray(5)
print(x, type(x))

# Memoryview
x = memoryview(bytes(5))
print(x, type(x))

# NoneType
x = None
print(x, type(x))